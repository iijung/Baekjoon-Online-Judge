T = int(input())
for i in range(T):
    x, y = map(int, input().split())

    d = y - x
    r = int(d ** 0.5)
    if r * (r - 1) < d <= r * r:
        print(r * 2 - 1)
    elif r * r < d <= r * (r+1):
        print(r * 2)
    else:
        print(r * 2 + 1)

